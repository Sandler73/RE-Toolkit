#
#         source "$RETOOLKIT_LIB_DIR/viz-helper.sh"
#         "$VENV_PY" - <<PYEOF
#         $(viz_helper_emit_svg_chrome_py)
#         $(viz_helper_emit_color_scale_py)
#         $(viz_helper_emit_treemap_py)
#         ...
#         PYEOF
#
#     All emitted visualizations are self-contained: inline SVG with no
#     external CDN reference, no JavaScript library dependency, and no network
