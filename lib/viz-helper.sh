# Synopsis:
#     Shared Python source emitters for the visualization layer.
#
# Description:
#     Defines functions that emit Python source code as text. The emitted
#     source is concatenated into the Python heredocs used by
#     stages/static/89-viz.sh and lib/aggregate.sh (write_cluster_graph).
