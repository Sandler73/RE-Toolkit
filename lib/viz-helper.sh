def html_escape(s):
    """Minimal HTML/SVG attribute-safe escape."""
    if s is None: return ""
    s = str(s)
    return (s.replace("&", "&amp;")
             .replace("<", "&lt;")
             .replace(">", "&gt;")
             .replace('"', "&quot;")
             .replace("'", "&#39;"))
